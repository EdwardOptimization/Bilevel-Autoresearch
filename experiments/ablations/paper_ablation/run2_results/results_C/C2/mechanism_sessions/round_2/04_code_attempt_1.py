import json
import logging
import math
from collections import defaultdict
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional, List, Tuple, Dict, Any


class PostGenerationAdjuster:
    """
    Adjusts LLM-generated parameter proposals based on momentum, elite pool patterns,
    and step calibration to improve search efficiency.

    The adjuster applies small, informed nudges to continuous parameter values
    before they are applied to the code, steering proposals toward regions that
    have historically performed well.
    """

    def __init__(
        self,
        momentum_tracker,
        step_calibrator,
        elite_pool,
        config,
        adjustment_enabled: bool = True,
        fallback_to_original: bool = True,
        max_relative_adjustment: float = 0.2,
    ):
        """
        Args:
            momentum_tracker: MomentumTracker instance for direction history
            step_calibrator: StepSizeCalibrator for step size calibration
            elite_pool: ElitePool for retrieving best recent configurations
            config: SearchConfig for parameter constraints
            adjustment_enabled: Master switch to enable/disable adjustments
            fallback_to_original: If True, return original value when adjustment yields invalid number
            max_relative_adjustment: Maximum proportional change (e.g., 0.2 = ±20%)
        """
        self.momentum_tracker = momentum_tracker
        self.step_calibrator = step_calibrator
        self.elite_pool = elite_pool
        self.config = config
        self.adjustment_enabled = adjustment_enabled
        self.fallback_to_original = fallback_to_original
        self.max_relative_adjustment = max_relative_adjustment

        # Initial allowlist of parameters to adjust
        self._allowlist = {"weight_decay", "learning_rate", "beta1", "beta2", "dropout"}

        # History for debugging and metrics
        self.adjustment_history: List[Dict[str, Any]] = []
        self._param_disable_count: Dict[str, int] = defaultdict(int)

        self.logger = logging.getLogger(__name__)

    def adjust_proposal(
        self,
        param_name: str,
        proposed_value: float,
        current_value: Optional[float],
        param_type: str = "continuous",
        iteration: int = 0,
    ) -> float:
        """
        Adjust a single proposed parameter value based on momentum and calibration.

        Args:
            param_name: Name of hyperparameter (e.g., 'weight_decay')
            proposed_value: Value generated by LLM
            current_value: Current value in code (None if parameter is new)
            param_type: 'continuous' or 'discrete' (only adjust continuous initially)
            iteration: Current iteration number

        Returns:
            Adjusted value (or original if adjustment not applicable/invalid)
        """
        # Step 1: Validation checks
        if not self.adjustment_enabled:
            return proposed_value

        if param_type != "continuous":
            return proposed_value

        if not self.should_adjust_param(param_name):
            return proposed_value

        # Step 2: Get adjustment direction
        direction = self._get_adjustment_direction(param_name, proposed_value)

        # If no direction can be determined, return original
        if direction == 0:
            return proposed_value

        # Step 3: Calculate adjustment magnitude
        magnitude = self._calculate_adjustment_magnitude(param_name, proposed_value, direction)

        # Step 4: Compute adjusted value
        adjusted = proposed_value + (direction * magnitude)
        adjusted = round(adjusted, 6)

        # Step 5: Validate and apply bounds
        validated = self._validate_and_bound(param_name, proposed_value, adjusted)
        if validated != adjusted:
            self.logger.debug(
                f"Adjustment for {param_name} invalid or out of bounds, "
                f"using {'original' if self.fallback_to_original else 'clamped'} value"
            )

        # Step 6: Log adjustment
        if validated != proposed_value:
            self.adjustment_history.append({
                "param_name": param_name,
                "proposed": proposed_value,
                "adjusted": validated,
                "direction": direction,
                "magnitude": magnitude,
                "iteration": iteration,
                "current": current_value,
            })
            self.logger.info(
                f"Adjusted {param_name}: {proposed_value} → {validated} "
                f"(dir={direction}, mag={magnitude:.6f})"
            )

        # Step 7: Return value
        return validated

    def should_adjust_param(self, param_name: str) -> bool:
        """
        Determine if parameter should be adjusted.
        Initially only continuous parameters with numeric ranges.
        """
        # Check allowlist
        if param_name not in self._allowlist:
            return False

        # Check if parameter is disabled due to poor performance
        if self._param_disable_count.get(param_name, 0) >= 5:
            self.logger.debug(f"Parameter {param_name} disabled due to poor adjustment performance")
            return False

        # Get parameter definition from config
        param_def = None
        for param in self.config.active_params:
            if param.name == param_name:
                param_def = param
                break

        if param_def is None:
            return False

        # Check if parameter has numeric type with range
        if param_def.type not in ("float", "int"):
            return False

        # Check if parameter has a valid range
        if not hasattr(param_def, 'range') or param_def.range is None:
            return False

        return True

    def _get_adjustment_direction(self, param_name: str, proposed_value: float) -> int:
        """
        Determine adjustment direction (1 for increase, -1 for decrease, 0 for none).
        """
        # Try momentum first
        momentum = self.momentum_tracker.get_momentum(param_name)
        if momentum is not None:
            # Check consistency of last 2-3 iterations
            # For simplicity, assume momentum_tracker provides consistent direction
            # if it returns a non-zero momentum
            if abs(momentum) > 1e-9:
                return 1 if momentum > 0 else -1

        # Fallback to elite pool comparison
        elite_configs = getattr(self.elite_pool, '_pool', [])
        if not elite_configs:
            return 0

        # Get best config from elite pool
        best_bpb, best_config, _, _ = elite_configs[0]
        best_value = best_config.get(param_name)

        if best_value is None:
            return 0

        # Move toward better value
        if proposed_value < best_value:
            return 1  # Increase toward better
        else:
            return -1  # Decrease toward better

    def _calculate_adjustment_magnitude(self, param_name: str, proposed_value: float, direction: int) -> float:
        """
        Calculate the adjustment magnitude based on step size and constraints.
        """
        # Get base step size
        base_step = self.step_calibrator.get_step_size(param_name)
        if base_step <= 0:
            return 0.0

        # Apply momentum amplification if available
        momentum = self.momentum_tracker.get_momentum(param_name)
        if momentum is not None:
            magnitude = base_step * (1.0 + abs(momentum))
        else:
            magnitude = base_step

        # Clamp to max relative adjustment
        max_abs_change = self.max_relative_adjustment * abs(proposed_value)
        magnitude = min(magnitude, max_abs_change)

        return magnitude

    def _validate_and_bound(self, param_name: str, proposed_value: float, adjusted_value: float) -> float:
        """
        Validate adjusted value and apply bounds if necessary.
        """
        # Check for NaN or infinite
        if not math.isfinite(adjusted_value):
            return proposed_value if self.fallback_to_original else self._clamp_to_bounds(param_name, adjusted_value)

        # Get parameter definition and bounds
        param_def = None
        for param in self.config.active_params:
            if param.name == param_name:
                param_def = param
                break

        if param_def is None:
            return proposed_value if self.fallback_to_original else adjusted_value

        # Get bounds
        if hasattr(param_def, 'range') and param_def.range:
            min_val, max_val = param_def.range
        else:
            # No bounds defined
            return adjusted_value

        # Check bounds
        if adjusted_value < min_val or adjusted_value > max_val:
            if self.fallback_to_original:
                return proposed_value
            else:
                return self._clamp_to_bounds(param_name, adjusted_value)

        return adjusted_value

    def _clamp_to_bounds(self, param_name: str, value: float) -> float:
        """
        Clamp value to parameter bounds.
        """
        param_def = None
        for param in self.config.active_params:
            if param.name == param_name:
                param_def = param
                break

        if param_def is None or not hasattr(param_def, 'range') or not param_def.range:
            return value

        min_val, max_val = param_def.range
        return max(min_val, min(value, max_val))

    def record_adjustment_performance(self, param_name: str, original_bpb: float, adjusted_bpb: float) -> None:
        """
        Record performance of adjustment to potentially disable poorly performing parameters.
        """
        if original_bpb < adjusted_bpb:  # Original was better
            self._param_disable_count[param_name] += 1
        else:
            self._param_disable_count[param_name] = max(0, self._param_disable_count[param_name] - 1)

    def get_adjustment_summary(self) -> str:
        """
        Generate a summary of adjustments for debugging.
        """
        if not self.adjustment_history:
            return "No adjustments applied."

        lines = ["## Post-Generation Adjustments Summary"]
        lines.append(f"Total adjustments: {len(self.adjustment_history)}")

        # Group by parameter
        by_param = defaultdict(list)
        for entry in self.adjustment_history:
            by_param[entry['param_name']].append(entry)

        for param_name, entries in by_param.items():
            lines.append(f"\n### {param_name} ({len(entries)} adjustments)")
            avg_change = sum(abs(e['adjusted'] - e['proposed']) for e in entries) / len(entries)
            lines.append(f"  Average absolute change: {avg_change:.6f}")
            lines.append(f"  Disable count: {self._param_disable_count.get(param_name, 0)}")

        return "\n".join(lines)