    def _propose(self, current_config: dict, iteration: int) -> dict:
        """Ask LLM to propose hyperparameter changes.



        Uses multi-candidate proposal (Improvement 2):
        1. LLM generates 3 candidates in one call.
        2. LLM picks the best candidate in a second call.
        This filters bad ideas with ~1 extra LLM call instead of wasting GPU time.



        Falls back to single-proposal mode if multi-candidate parsing fails.
        """

        from core.llm_client import parse_json_response



        self._proposal_count += 1



        crash_warnings = self.crash_memory.get_warning_text()
        guidance = self.search_config.guidance



        if self.simple_mode:

            # simple_mode: skip all Level 2 mechanisms — use single PROPOSE_PROMPT directly
            prompt = PROPOSE_PROMPT.format(
                current_config=json.dumps(current_config, indent=2),
                trace_summary=self.trace.summary(),
                strategy=self.search_config.strategy,
                active_params=", ".join(self.search_config.active_params),
                frozen_params=", ".join(self.search_config.frozen_params) or "(none)",
                guidance=guidance,
                crash_warnings=crash_warnings,
                momentum_signals="",
            )

            raw = self.client.call(prompt, system=PROPOSE_SYSTEM, max_tokens=1000)
            result = parse_json_response(raw)
            if not isinstance(result, dict) or "raw_content" in result:
                logger.warning(f"[Iter {iteration}] Failed to parse proposal: {raw[:200]}")
                return {"changes": {}, "hypothesis": "parse failure"}
            changes = result.get("changes", {})
            active = set(self.search_config.active_params)
            filtered = {k: v for k, v in changes.items() if k in active}
            if len(filtered) < len(changes):
                dropped = set(changes) - active
                logger.warning(f"[Iter {iteration}] Dropped frozen params from proposal: {dropped}")
            result["changes"] = filtered
            return result



        momentum_signals = self.momentum.get_momentum_text()
        elite_pool_text = self.elite_pool.get_elite_text()
        step_size_text = self.step_calibrator.get_step_size_text()
        epistasis_text = self.epistasis_map.get_epistasis_text()
        grn_text = self.grn.get_grn_text()
        knockout_text = self.knockout_screen.get_importance_text()
        operon_text = self.transgenic.get_operon_text()
        stratigraphy_text = self.stratigraphy.get_stratigraphy_text()
        fossil_text = self.fossil_record.get_fossil_text()
        context_text = self.context_stratigraphy.get_context_text()
        excavation_text = self.excavation_grid.get_excavation_text()
        compost_text = self.compost_heap.get_compost_text()
        soil_health_text = self.soil_health.get_health_text(self.search_config.active_params)
        seasonal_text = self.seasonal_cycling.get_season_text(self.search_config.active_params)
        perennial_text = self.perennial_classifier.get_perennial_text(self.search_config.active_params)
        survey_text = self.survey_tiling.get_coverage_text()
        seeing_text = self.adaptive_optics.get_seeing_text()
        too_text = self.target_of_opportunity.get_too_text()
        spectral_text = self.spectral_decomposition.get_attribution_text()
        roughness_text = self.parallax_estimator.get_roughness_text()
        calibration_text = self.back_translation.get_calibration_text()
        redundancy_text = self.semantic_equivalence.get_redundancy_summary()
        register_text = self.register_adaptation.get_register_text()
        domestication_text = self.domestication.get_domestication_text(iteration)
        post_edit_text = self.post_editing.get_post_edit_text()

        # Improvement 12: Check for plateau and inject diversification directive
        is_plateau, plateau_directive = self.plateau_detector.check_plateau()
        if is_plateau:
            guidance = guidance + "\n" + plateau_directive
            logger.info(f"[Iter {iteration}] PLATEAU DETECTED — forcing diversification")



        # Improvement 14+15: Exploration budget — risk-aware exploration
        is_exploration_turn = (self._proposal_count % self._exploration_frequency == 0)
        if is_exploration_turn and not is_plateau:
            exploration_directive = (
                "\n## EXPLORATION TURN (risk-aware)\n"
                "This is a mandatory exploration iteration. Try a parameter you haven't changed recently.\n"
                "\nSAFE to explore (low risk with short training runs):\n"
                "- WEIGHT_DECAY, WARMUP_RATIO, WARMDOWN_RATIO, FINAL_LR_FRAC — schedule params\n"
                "- EMBEDDING_LR, UNEMBEDDING_LR — secondary LR params\n"
                "- ADAM_BETAS — optimizer momentum\n"
                "\nDANGEROUS to explore (high risk with short training runs):\n"
                "- DEPTH — changes model capacity dramatically, needs longer training to converge\n"
                "- TOTAL_BATCH_SIZE — large changes cause gradient scale mismatch\n"
                "- HEAD_DIM — changes model architecture fundamentally\n"
                "\nMake a MODERATE change to a safe-to-explore parameter you haven't tried recently."
            )

            guidance = guidance + "\n" + exploration_directive
            logger.info(f"[Iter {iteration}] EXPLORATION TURN — risk-aware exploration")



        # --- Attempt multi-candidate proposal ---
        multi_prompt = MULTI_CANDIDATE_PROMPT.format(
            current_config=json.dumps(current_config, indent=2),
            trace_summary=self.trace.summary(),
            strategy=self.search_config.strategy,
            active_params=", ".join(self.search_config.active_params),
            frozen_params=", ".join(self.search_config.frozen_params) or "(none)",
            guidance=guidance,
            crash_warnings=crash_warnings,
            momentum_signals=momentum_signals,
            elite_pool=elite_pool_text,
            step_size_recs=step_size_text,
            epistasis_signals=epistasis_text,
            grn_signals=grn_text,
            knockout_importance=knockout_text,
            operon_info=operon_text,
            stratigraphy_info=stratigraphy_text,
            fossil_info=fossil_text,
            context_info=context_text,
            excavation_info=excavation_text,
            survey_coverage=survey_text,
            seeing_conditions=seeing_text,
            too_alert=too_text,
            spectral_analysis=spectral_text,
            landscape_roughness=roughness_text,
            calibration_report=calibration_text,
            redundancy_map=redundancy_text,
            register_context=register_text,
            domestication_context=domestication_text,
            post_edit_constraints=post_edit_text,
            compost_info=compost_text,
            soil_health_info=soil_health_text,
            seasonal_info=seasonal_text,
            perennial_info=perennial_text,
        )



        raw = self.client.call(multi_prompt, system=PROPOSE_SYSTEM, max_tokens=2000)
        multi_result = parse_json_response(raw)



        candidates = None
        if isinstance(multi_result, dict) and "candidates" in multi_result:
            raw_candidates = multi_result["candidates"]
            if isinstance(raw_candidates, list) and len(raw_candidates) >= 2:
                # Validate each candidate has changes
                valid = [c for c in raw_candidates if isinstance(c, dict) and c.get("changes")]
                if len(valid) >= 2:
                    candidates = valid[:3]  # cap at 3



        if candidates:
            # Improvement 10+13: Inject crossover candidate from elite pool
            # (only every Nth iteration to give LLM more creative slots)
            is_crossover_turn = (self._proposal_count % self._crossover_frequency == 0)



            # Improvement 21: Inject knockout candidate every Nth iteration
            is_knockout_turn = (self._proposal_count % self._knockout_frequency == 0)



            # Improvement 22: Alternate between crossover and transgenic insertion
            # Even crossover turns use classic crossover, odd ones use transgenic
            use_transgenic = (self._proposal_count // self._crossover_frequency) % 2 == 1



            if is_knockout_turn and not is_exploration_turn:
                # Knockout takes priority — it's a diagnostic experiment
                knockout = self.knockout_screen.suggest_knockout(
                    self.search_config.active_params
                )

                if knockout and len(candidates) >= 3:
                    logger.info(f"[Iter {iteration}] Injecting knockout candidate: {knockout['changes']}")
                    candidates[-1] = knockout
                elif knockout and len(candidates) < 3:
                    candidates.append(knockout)
                    logger.info(f"[Iter {iteration}] Added knockout candidate: {knockout['changes']}")
                else:
                    logger.info(f"[Iter {iteration}] No knockout candidate available")
            elif is_crossover_turn and not is_exploration_turn:
                if use_transgenic:
                    # Improvement 22: Transgenic insertion — transplant exact operon
                    elite_configs = [
                        (bpb, cfg) for bpb, cfg, _, _ in self.elite_pool._pool
                    ]
                    transgenic = self.transgenic.generate_transgenic_candidate(
                        current_config, elite_configs, self.search_config.active_params
                    )

                    if transgenic and len(candidates) >= 3:
                        logger.info(f"[Iter {iteration}] Injecting transgenic candidate: {transgenic['changes']}")
                        candidates[-1] = transgenic
                    elif transgenic and len(candidates) < 3:
                        candidates.append(transgenic)
                        logger.info(f"[Iter {iteration}] Added transgenic candidate: {transgenic['changes']}")
                    else:
                        # Fallback to classic crossover if transgenic not possible
                        crossover = self.elite_pool.generate_crossover(
                            current_config, self.search_config.active_params
                        )

                        if crossover and len(candidates) >= 3:
                            logger.info(f"[Iter {iteration}] Injecting crossover (transgenic unavailable): {crossover['changes']}")
                            candidates[-1] = crossover
                        elif crossover and len(candidates) < 3:
                            candidates.append(crossover)
                else:
                    # Classic crossover
                    crossover = self.elite_pool.generate_crossover(
                        current_config, self.search_config.active_params
                    )

                    if crossover and len(candidates) >= 3:
                        logger.info(f"[Iter {iteration}] Injecting crossover candidate: {crossover['changes']}")
                        candidates[-1] = crossover
                    elif crossover and len(candidates) < 3:
                        candidates.append(crossover)
                        logger.info(f"[Iter {iteration}] Added crossover candidate: {crossover['changes']}")
            else:
                # Improvement 24+26: On non-crossover/knockout turns, try
                # Lazarus revival (every 6th) or excavation (every 7th)
                is_lazarus_turn = (self._proposal_count % 6 == 0)
                is_excavation_turn = (self._proposal_count % 7 == 0)



                injected = False
                if is_lazarus_turn and not is_exploration_turn:
                    revival = self.fossil_record.suggest_revival(
                        current_config, self.trace.best_bpb,
                        self.search_config.active_params,
                    )

                    if revival:
                        if len(candidates) >= 3:
                            candidates[-1] = revival
                        else:
                            candidates.append(revival)
                        logger.info(f"[Iter {iteration}] Injecting Lazarus revival candidate: {revival['changes']}")
                        injected = True



                if not injected and is_excavation_turn and not is_exploration_turn:
                    excavation = self.excavation_grid.suggest_excavation(
                        current_config, self.search_config.active_params,
                    )

                    if excavation:
                        if len(candidates) >= 3:
                            candidates[-1] = excavation
                        else:
                            candidates.append(excavation)
                        logger.info(f"[Iter {iteration}] Injecting excavation candidate: {excavation['changes']}")
                        injected = True



                if not injected:
                    logger.info(f"[Iter {iteration}] Skipping crossover/transgenic/knockout/lazarus/excavation (proposal #{self._proposal_count})")



            logger.info(f"[Iter {iteration}] Got {len(candidates)} candidates, asking LLM to pick best")



            # Build picker prompt
            candidates_text_lines = []
            for i, c in enumerate(candidates):
                candidates_text_lines.append(
                    f"### Candidate {i}\n"
                    f"Changes: {json.dumps(c['changes'])}\n"
                    f"Hypothesis: {c.get('hypothesis', 'N/A')}\n"
                    f"Risk: {c.get('risk', 'unknown')}\n"
                )

            candidates_text = "\n".join(candidates_text_lines)



            pick_prompt = PICK_CANDIDATE_PROMPT.format(
                best_bpb=f"{self.trace.best_bpb:.6f}",
                candidates_text=candidates_text,
            )



            pick_raw = self.client.call(pick_prompt, system=PROPOSE_SYSTEM, max_tokens=500)
            pick_result = parse_json_response(pick_raw)



            picked_idx = 0  # default to first (most promising) if parsing fails
            if isinstance(pick_result, dict) and "pick" in pick_result:
                try:
                    idx = int(pick_result["pick"])
                    if 0 <= idx < len(candidates):
                        picked_idx = idx
                except (ValueError, TypeError):
                    pass



            chosen = candidates[picked_idx]
            logger.info(
                f"[Iter {iteration}] Picked candidate {picked_idx}: {chosen['changes']} "
                f"(reason: {pick_result.get('reasoning', 'N/A')[:100]})"
            )



            # Validate: only active params
            changes = chosen.get("changes", {})
            active = set(self.search_config.active_params)
            filtered = {k: v for k, v in changes.items() if k in active}
            if len(filtered) < len(changes):
                dropped = set(changes) - active
                logger.warning(f"[Iter {iteration}] Dropped frozen params from proposal: {dropped}")
            chosen["changes"] = filtered



            return chosen



        # --- Fallback: single proposal (original behavior) ---
        logger.info(f"[Iter {iteration}] Multi-candidate failed, falling back to single proposal")



        prompt = PROPOSE_PROMPT.format(
            current_config=json.dumps(current_config, indent=2),
            trace_summary=self.trace.summary(),
            strategy=self.search_config.strategy,
            active_params=", ".join(self.search_config.active_params),
            frozen_params=", ".join(self.search_config.frozen_params) or "(none)",
            guidance=guidance,
            crash_warnings=crash_warnings,
            momentum_signals=momentum_signals,
        )



        raw = self.client.call(prompt, system=PROPOSE_SYSTEM, max_tokens=1000)
        result = parse_json_response(raw)



        if not isinstance(result, dict) or "raw_content" in result:
            logger.warning(f"[Iter {iteration}] Failed to parse proposal: {raw[:200]}")
            return {"changes": {}, "hypothesis": "parse failure"}



        # Validate: only active params
        changes = result.get("changes", {})
        active = set(self.search_config.active_params)
        filtered = {k: v for k, v in changes.items() if k in active}
        if len(filtered) < len(changes):
            dropped = set(changes) - active
            logger.warning(f"[Iter {iteration}] Dropped frozen params from proposal: {dropped}")
        result["changes"] = filtered



        return result



    def _apply_changes(self, code: str, changes: dict) -> str:
        """Replace hyperparameter values in train.py code."""

        for param, value in changes.items():
            # Match lines like: PARAM_NAME = value  # comment
            pattern = rf'^({re.escape(param)}\s*=\s*)(.+?)(\s*#.*)?$'
            safe_value = str(value).replace('\\', '\\\\')
            replacement = rf'\g<1>{safe_value}\3'
            code = re.sub(pattern, replacement, code, count=1, flags=re.MULTILINE)
        return code



    def _inject_time_budget(self, code: str, override_budget: int | None = None) -> str:
        """Override TIME_BUDGET if search_config specifies a non-default value.



        Idempotent — safe to call multiple times on the same code.



        Args:
            override_budget: If set, use this value instead of search_config.time_budget.
                             Used by quick-test to inject a 15-second budget.
        """

        budget = override_budget if override_budget is not None else self.search_config.time_budget
        if budget != 300:
            # Step 1: Ensure the import uses `as _TIME_BUDGET` alias
            if "TIME_BUDGET as _TIME_BUDGET" not in code:
                code = code.replace(
                    "from prepare import MAX_SEQ_LEN, TIME_BUDGET,",
                    "from prepare import MAX_SEQ_LEN, TIME_BUDGET as _TIME_BUDGET,",
                    1,
                )



            # Step 2: Remove any existing override lines (idempotent cleanup)
            code = re.sub(
                r'\nTIME_BUDGET = \d+\s*#[^\n]*\n',
                '\n',
                code,
            )



            # Step 3: Insert the override ONCE before the GPT Model section
            code = code.replace(
                "# ---------------------------------------------------------------------------\n# GPT Model",
                f"TIME_BUDGET = {budget}  # overridden by bilevel runner\n\n"
                "# ---------------------------------------------------------------------------\n# GPT Model",
                1,
            )

        return code



    def _extract_hyperparams(self, code: str) -> dict:
        """Parse current hyperparameter values from train.py code."""

        config = {}
        for param in HYPERPARAM_NAMES:
            pattern = rf'^{re.escape(param)}\s*=\s*(.+?)(?:\s*#.*)?$'
            match = re.search(pattern, code, re.MULTILINE)
            if match:
                config[param] = match.group(1).strip()
        return config



    def _run_training(
        self, train_path: Path, iter_dir: Path, quick_test: bool = False
    ) -> dict | None:
        """Execute train.py and parse results. Returns None on crash.



        Args:
            quick_test: If True, run a 15-second smoke test instead of full training.
                        Returns dict with 'quick_test_diverged' key if loss is abnormally high.
        """

        if quick_test:
            # Improvement 4: Write quick test to work_dir (not iter_dir) so that
            # `from prepare import ...` resolves correctly. The previous bug wrote
            # the file to iter_dir (inside artifacts/) where prepare.py doesn't exist.
            code = self._apply_changes(
                self.current_code,
                self._extract_changes_from_file(train_path),
            )

            code = self._inject_time_budget(code, override_budget=self.QUICK_TEST_BUDGET)

            # Write to work_dir so imports resolve correctly
            quick_path = self.work_dir / "train_quicktest.py"
            quick_path.write_text(code, encoding="utf-8")
            # Also save a copy in iter_dir for debugging
            (iter_dir / "train_quicktest.py").write_text(code, encoding="utf-8")
            run_path = quick_path
            log_name = "quicktest.log"
            timeout = self.QUICK_TEST_BUDGET * 6  # 90s max for 15s test (compile overhead)
        else:
            run_path = train_path
            log_name = "run.log"
            timeout = self.search_config.time_budget * 3



        log_path = iter_dir / log_name



        try:
            # Use the work_dir's .venv python if available (has torch),
            # otherwise fall back to current interpreter
            venv_python = self.work_dir / ".venv" / "bin" / "python"
            python_cmd = str(venv_python) if venv_python.exists() else sys.executable
            result = subprocess.run(
                [python_cmd, str(run_path)],
                capture_output=True, text=True,
                timeout=timeout,
                cwd=str(self.work_dir),
            )

            log_path.write_text(result.stdout + "\n" + result.stderr, encoding="utf-8")



            if result.returncode != 0:
                logger.warning(
                    f"Training failed (exit {result.returncode})"
                    f"{' [quick test]' if quick_test else ''}"
                )

                (iter_dir / ("quicktest_error.txt" if quick_test else "error.txt")).write_text(
                    result.stderr[-2000:], encoding="utf-8"
                )

                return None



            parsed = self._parse_results(result.stdout)



            if quick_test and parsed is not None:
                # Check for divergence: abnormally high loss or NaN
                val_bpb = parsed.get("val_bpb", 0)
                if val_bpb != val_bpb:  # NaN check
                    parsed["quick_test_diverged"] = True
                    logger.warning("[Quick test] NaN detected in val_bpb")
                elif val_bpb > self.QUICK_TEST_LOSS_THRESHOLD:
                    parsed["quick_test_diverged"] = True
                    logger.warning(f"[Quick test] Loss {val_bpb:.4f} > threshold {self.QUICK_TEST_LOSS_THRESHOLD}")
                else:
                    parsed["quick_test_diverged"] = False



            # For quick test, if parsing fails entirely, check stderr for FAIL/OOM signals
            if quick_test and parsed is None:
                output = result.stdout + result.stderr
                if any(kw in output.upper() for kw in ["FAIL", "OOM", "OUT OF MEMORY", "NAN", "CUDA ERROR"]):
                    return None
                # No val_bpb but no obvious error — could be too short to produce output
                # Treat as passing (not enough data to judge)
                return {"val_bpb": 0, "quick_test_diverged": False}



            return parsed



        except subprocess.TimeoutExpired:
            logger.warning(f"Training timed out{' [quick test]' if quick_test else ''}")
            return None
        except Exception as e:
            logger.warning(f"Training error: {e}")
            return None



    def _extract_changes_from_file(self, train_path: Path) -> dict:
        """Extract the hyperparameter changes applied to a modified train.py file.



        Compares the modified file against self.current_code to find what changed.
        """

        modified_code = train_path.read_text(encoding="utf-8")
        current_config = self._extract_hyperparams(self.current_code)
        modified_config = self._extract_hyperparams(modified_code)
        changes = {}
        for param in modified_config:
            if param in current_config and modified_config[param] != current_config[param]:
                changes[param] = modified_config[param]
        return changes



    def _parse_results(self, stdout: str) -> dict | None:
        """Parse val_bpb and other metrics from training output."""

        metrics = {}
        for line in stdout.splitlines():
            for key in ["val_bpb", "peak_vram_mb", "training_seconds",
                        "num_params_M", "depth", "total_seconds"]:
                if line.startswith(f"{key}:"):
                    try:
                        val = float(line.split(":")[1].strip())
                        # Normalize key names
                        norm_key = key.lower().replace("_m", "_m")
                        if key == "num_params_M":
                            norm_key = "num_params_m"
                        metrics[norm_key] = val
                    except (ValueError, IndexError):
                        pass



        if "val_bpb" not in metrics:
            return None
        return metrics

