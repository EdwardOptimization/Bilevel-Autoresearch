"""Inner loop runner for training optimization.
One iteration = propose config change → modify train.py → run training → parse val_bpb → keep/discard.
The search behavior is controlled by SearchConfig, which the outer loop modifies.
Structural improvements (Level 2):
  1. Crash memory — track which params caused crashes and warn the LLM in proposals.
  2. Multi-candidate proposal — LLM generates 3 candidates, then picks best, before GPU time.
  3. Quick-test pre-filter — 15-second smoke test before committing to a full training run.
Structural improvements (Level 2 — Round 2):
  4. Fixed quick-test path bug — quick test file now written to work_dir (not iter_dir)
     so that `from prepare import ...` resolves correctly.
  5. Infrastructure vs training crash classification — if quick test fails due to an
     import error or syntax error (infrastructure bug), skip the quick test and go
     straight to full training instead of wrongly recording a crash.
  6. Momentum tracking — track which parameter change directions led to improvements
     and inject this signal into the proposal prompt so the LLM learns from patterns.
Structural improvements (Level 2 — Round 3):
  7. Simulated annealing acceptance — instead of strict greedy keep/discard, accept
     slightly worse results with decreasing probability as iterations progress. This
     enables the search to escape local minima. Temperature starts at 0.01 and cools.
  8. Revert-to-best on stagnation — if no improvement is found for N consecutive
     iterations, revert the working code to the best-known config. This prevents
     the search from drifting too far from the best solution.
Structural improvements (Level 2 — Round 4):
  9. Elite pool — maintain a pool of top-K configs (by val_bpb), not just the single
     best. The pool is shown to the LLM during proposals to help identify patterns
     across good configs and suggest interpolations/extrapolations.
Structural improvements (Level 2 — Round 5):
  10. Automatic crossover candidate — one of the 3 multi-candidates is now automatically
      generated by interpolating between the two best configs in the elite pool. This
      replaces one LLM-generated candidate with a systematic exploration of the space
      between proven good configs, similar to genetic algorithm crossover.
Structural improvements (Level 2 — Round 6):
  11. Adaptive step-size calibration — tracks the magnitude of parameter changes that
      led to improvements vs regressions, and injects recommended step-size ranges into
      the proposal prompt. This prevents the LLM from making changes that are too timid
      (wasting iterations on negligible tweaks) or too aggressive (causing crashes).
      Also adds validation to crossover candidates to prevent generating clearly invalid
      values (e.g., non-power-of-2 batch sizes, values outside observed elite ranges).
Structural improvements (Level 2 — Round 7):
  12. Plateau detection with forced diversification — detects when the search is stuck
      on the same parameters with diminishing returns, and forces the next proposal to
      explore a DIFFERENT parameter set. This breaks the common pattern where the LLM
      keeps making tiny LR tweaks after finding a good LR range, wasting iterations.
Structural improvements (Level 2 — Round 8):
  13. Crossover frequency limiter — crossover is only injected every 3rd iteration, not
      every iteration. This gives the LLM more chances to propose novel ideas instead of
      the LLM always selecting the "safe" crossover candidate. When crossover is not
      injected, the LLM gets all 3 candidate slots for creative proposals.
  14. Exploration budget — every 3rd iteration is marked as a mandatory "exploration"
      turn where the LLM is instructed to try a bold/novel change rather than incremental
      optimization. This ensures the search doesn't get stuck in local optima.
Structural improvements (Level 2 — Round 9):
  15. Risk-aware exploration — replaces the blind "bold exploration" directive with a
      risk-aware one that considers which parameters are safe to explore with short time
      budgets. Architectural parameters (DEPTH, HEAD_DIM) are marked as high-risk since
      they change model capacity and need longer training. Schedule and LR parameters are
      marked as safe for exploration. Also reduces exploration frequency to every 4th
      iteration since most wins come from incremental LR optimization.
  16. Regression-safe acceptance — tightens the SA acceptance to reject changes that
      are more than 0.003 bpb worse, even if SA would accept them. This prevents the
      search from accepting large regressions that waste subsequent iterations trying
      to recover.
Structural improvements (Level 2 — Round 10):
  17. Outer loop freeze limit — limits the outer loop to freezing at most 5 params per
      cycle. Previously it could freeze 11+ params at once, leaving the LLM with almost
      no options. This ensures at least 5+ params remain active even after outer loop
      analysis. The outer loop prompt is also updated to emphasize this constraint.
  18. Improved momentum signal with confidence — momentum signals now include a confidence
      indicator based on sample size and consistency. Low-confidence signals are labeled
      as "tentative" so the LLM doesn't over-react to noisy early data.
Structural improvements (Level 2 — Round 11, Molecular Genetics):
  19. Epistasis mapping — tracks pairwise parameter interaction effects. When two params
      are changed together, measures whether the joint effect is better (synergistic) or
      worse (antagonistic) than the sum of their individual effects. Advises the LLM
      which pairs to change together vs separately.
  20. Gene regulatory network — learns parameter dependency rules from elite config
      correlations. When param A is high in elites, is param B also high (positive
      regulation) or low (negative regulation)? Injects compensatory change recommendations
      so the LLM knows "if you increase LR, also increase WEIGHT_DECAY."
  21. Knockout screen — periodically resets a parameter to its default value to measure
      its importance. Essential parameters (large regression on knockout) are protected;
      dispensable parameters (no effect) can be safely frozen. Injects knockout candidates
      into the multi-candidate pool every 5th iteration.
  22. Transgenic insertion — transplants exact parameter blocks ("operons") from elite
      configs into the current config. Unlike crossover (which interpolates), transgenic
      insertion preserves the internal consistency of proven-good parameter groups. Operons
      include lr_operon (all LRs), schedule_operon (warmup/warmdown/final), etc.
Structural improvements (Level 2 — Round 12, Archaeology/Paleontology):
  23. Stratigraphic record — layer-by-layer reconstruction of how the current config
      was built. Each "kept" iteration forms a stratum. Tracks which parameters are
      ancient/foundation (changed early, never modified since) vs recent/experimental.
      Injects this provenance chain into the proposal prompt so the LLM understands
      which values are battle-tested pillars vs fragile recent additions.
  24. Fossil record / Lazarus taxa — preserves ALL configs ever tried (not just elite
      survivors). Periodically suggests reviving extinct configs that showed strong
      relative improvement or explored parameter regions never revisited. Lazarus
      revival candidates are injected into the multi-candidate pool every 6th iteration.
  25. Context stratigraphy — tracks the full parameter context surrounding each
      successful/failed change. Identifies context-dependent patterns like "increasing
      MATRIX_LR worked when DEPTH<=10 but failed when DEPTH>=12." Helps the LLM
      understand that parameter values are not universally good or bad — their
      effectiveness depends on the surrounding context.
  26. Excavation grid — divides each parameter's plausible range into bins and tracks
      sampling density. Identifies blind spots (bins never sampled) and suggests
      systematic excavation of under-explored regions. Prevents the search from
      clustering around early successes and ignoring potentially good regions.

Structural improvements (Level 2 — Observational Astronomy):
  31. Survey tiling (SDSS-style) — tracks which regions of each parameter's range have
      been "observed" (proposed) vs unexplored ("dark sky"). Divides each parameter into
      bins and reports coverage maps. Guides the LLM to systematically tile the parameter
      sky rather than staring at the same patch repeatedly.
  32. Adaptive optics (noise correction) — estimates the measurement noise floor from
      historical val_bpb variance using median absolute deviation. Computes signal-to-noise
      ratios for each improvement. Warns when deltas are below the "seeing limit" (noise
      floor) and recommends minimum change magnitudes to beat the noise.
  33. Target of opportunity (surprise follow-up) — detects when an improvement contradicts
      the momentum signal (parameter historically hurt but now helped). These "transient
      events" indicate the search may have found a new, better region. Generates alerts
      that direct the LLM to follow up on the surprise instead of returning to routine
      exploration.
  34. Spectral decomposition (per-parameter attribution) — uses historical single-parameter
      experiments as "reference spectra" to decompose multi-parameter results into per-
      parameter contributions. Identifies which parameters are the strongest levers vs
      neutral passengers, improving the quality of all other signals.
  35. Parallax estimation (landscape roughness) — measures how rapidly val_bpb changes per
      unit of parameter change for each parameter. High roughness = jagged landscape
      requiring small steps; low roughness = smooth landscape allowing large steps. Gives
      a physics-based reason for step-size selection beyond what the step-size calibrator
      provides.

Structural improvements (Level 2 — Translation Theory):
  36. Back-translation — tracks the LLM's predicted outcome direction vs actual result.
      When predictions are systematically wrong for a parameter, the LLM's internal model
      of the loss landscape is miscalibrated. Injects calibration errors into the prompt
      so the LLM can correct its reasoning biases.
  37. Semantic equivalence — canonicalizes proposals to (param, direction, magnitude_bin)
      and detects when the LLM re-proposes something already tried under different wording.
      Prevents wasted iterations on semantically identical proposals.
  38. Register adaptation — adaptively switches prompt register (formal/informal/terse)
      based on which style produces the best outcomes. Formal for exploitation, informal
      for exploration, terse for data-driven phases. Switches on stagnation.
  39. Domestication vs foreignization — detects comfort-zone patterns (same param changed
      repeatedly, round-number bias, parameter fixation) and injects foreignization frames
      (ratio thinking, gradient flow, information bottleneck, loss landscape geometry,
      scaling law reasoning) to force novel reasoning.
  40. Post-editing — applies rule-based constraint checks to the LLM's raw proposal before
      committing to a GPU run: batch sizes must be powers of 2, warmup+warmdown<=1.0,
      LRs must be positive, DEPTH must be a positive integer, etc. Fixes safe violations
      automatically and warns the LLM about recurring constraint violations.
"""

